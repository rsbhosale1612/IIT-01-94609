def cal_recr(len, br):
    area = len * br
    print("Area of Rect = ",area)

def cal_seq(len):
    area = len * len
    print("Area of square = ",area)

def cal_trai(len, br):
    area = 1/2 * (len * br)
    print("Area of Traiangle = ",area)

